#3.Comparison operators
n = 20
m = 10
# Equal to
print(n == m)  

# Not equal to
print(n != m)  

# Greater than
print(n > m)  

# Less than
print(n < m)  

# Greater than or equal to
print(n >= m)  

# Less than or equal to
print(n <= m)  


#4.Logical operators
p = True
q = False
# Logical AND
print(p and q)  

# Logical OR
print(p or q)  

# Logical NOT
print(not q) 


#5.Bitwise operators
u = 10  
v = 7  

# Bitwise AND
print(u & v)  

# Bitwise OR
print(u | v)  

# Bitwise XOR
print(u ^ v)  

# Bitwise NOT
print(~u) 

# Bitwise Left Shift
print(u << 2)  

# Bitwise Right Shift
print(u >> 2) 


